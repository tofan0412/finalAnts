package ants.com.common.model;

public class IpHistoryVo {
	private String iphistIdx; 
	private String memId; 
	private String ipAddr; 
	private String iphistDate;

	public String getIpHistIdx() {
		return iphistIdx;
	}
	public void setIpHistIdx(String ipHistIdx) {
		this.iphistIdx = ipHistIdx;
	}
	public String getMemId() {
		return memId;
	}
	public void setMemId(String memId) {
		this.memId = memId;
	}
	public String getIpAddr() {
		return ipAddr;
	}
	public void setIpAddr(String ipAddr) {
		this.ipAddr = ipAddr;
	}
	public String getIphistDate() {
		return iphistDate;
	}
	public void setIphistDate(String iphistDate) {
		this.iphistDate = iphistDate;
	}
}
